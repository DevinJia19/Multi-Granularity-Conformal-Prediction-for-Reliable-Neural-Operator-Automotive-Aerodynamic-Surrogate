#!/usr/bin/env python3
"""Compare Global CP, Point-based CP, and Case-based CP for pressure/WSS fields.

Inputs are .npz files generated by inference_on_zarr.py with:
    cp_output.save_pointwise_npz=true

Each .npz should contain:
    pred, target, sigma
with channels:
    0 pressure, 1 wss_x, 2 wss_y, 3 wss_z

Definitions implemented here:
1) global_abs
   Calibration score: |y - y_hat|
   Interval: y_hat ± q_abs
   This gives one fixed absolute half-width per channel, shared by all points.

2) point_sigma
   Calibration score: |y - y_hat| / max(sigma, eps)
   Interval: y_hat ± q_sigma * sigma
   This is the sigma-normalized point-adaptive CP used by your current script.

3) case_sigma
   For each calibration case, first compute point scores:
       s_i = |y_i - y_hat_i| / max(sigma_i, eps)
   Then reduce each case to one case score per channel:
       default: within-case conformal quantile at 1-alpha
       optional: max / mean
   Then compute conformal quantile across calibration cases.
   Interval: y_hat ± q_case * sigma

Outputs:
    summary.json
    comparison_table.csv
    per_case_metrics.csv
    qhat.json

Calibration inputs:
    --calib-dir PATH          single directory of *.npz
    --calib-glob PATTERN      e.g. runs/cvplus/fold_*/cvplus_oof/fold_*/*.npz
                              (directories or .npz files matched by glob)

Optional subsampling (global_abs and point_sigma only):
    --score-sample-per-file N  randomly sample N points per calibration .npz
                               before pooling scores for qhat; case_sigma is unchanged
    --score-sample-seed SEED   RNG seed for reproducible per-file sampling
"""

from __future__ import annotations

import argparse
import csv
import json
import math
from pathlib import Path
from typing import Dict, List, Tuple

import numpy as np

CHANNELS = ["pressure", "wss_x", "wss_y", "wss_z"]
MODES = ["global_abs", "point_sigma", "case_sigma"]


def as_points(x: np.ndarray) -> np.ndarray:
    x = np.asarray(x)
    if x.ndim == 3 and x.shape[0] == 1:
        x = x[0]
    if x.ndim != 2:
        raise ValueError(f"Expected (N,C) or (1,N,C), got {x.shape}")
    if x.shape[1] < 4:
        raise ValueError(f"Expected at least 4 channels, got {x.shape}")
    return x[:, :4]


def finite_sample_quantile_1d(scores: np.ndarray, alpha: float) -> float:
    """Split-conformal finite-sample quantile for a 1D array."""
    scores = np.asarray(scores)
    scores = scores[np.isfinite(scores)]
    n = scores.shape[0]
    if n < 1:
        raise ValueError("No finite calibration scores.")
    k = int(math.ceil((n + 1) * (1.0 - alpha)))
    k = min(max(k, 1), n)
    return float(np.partition(scores, k - 1)[k - 1])


def finite_sample_quantile_2d_channelwise(scores: np.ndarray, alpha: float) -> np.ndarray:
    """Split-conformal finite-sample quantile for shape (N,4), channel-wise."""
    scores = np.asarray(scores)
    out = []
    for ch in range(4):
        out.append(finite_sample_quantile_1d(scores[:, ch], alpha))
    return np.asarray(out, dtype=np.float64)


def list_npz(path: Path) -> List[Path]:
    files = sorted(path.glob("*.npz"))
    if not files:
        raise FileNotFoundError(f"No .npz files found in {path}")
    return files


def list_npz_from_glob(pattern: str) -> List[Path]:
    """Collect .npz files from a glob (directories or direct .npz paths)."""
    files: List[Path] = []
    for p in sorted(Path().glob(pattern)):
        if p.is_dir():
            files.extend(sorted(p.glob("*.npz")))
        elif p.suffix == ".npz":
            files.append(p)
    if not files:
        raise FileNotFoundError(f"No .npz files matched: {pattern}")
    return files


def load_pred_target_sigma(npz_path: Path, eps: float) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
    with np.load(npz_path, allow_pickle=True) as data:
        for key in ("pred", "target", "sigma"):
            if key not in data:
                raise KeyError(f"{npz_path} does not contain key '{key}'")
        pred = as_points(data["pred"]).astype(np.float32, copy=False)
        target = as_points(data["target"]).astype(np.float32, copy=False)
        sigma = as_points(data["sigma"]).astype(np.float32, copy=False)
        sigma = np.maximum(sigma, eps)
    return pred, target, sigma


def sample_point_indices(
    n_points: int,
    n_sample: int,
    file_idx: int,
    seed: int,
) -> np.ndarray:
    """Deterministic per-file subsample indices (same for global_abs / point_sigma)."""
    if n_sample <= 0 or n_points <= n_sample:
        return np.arange(n_points, dtype=np.int64)
    rng = np.random.default_rng(seed + file_idx)
    return rng.choice(n_points, size=n_sample, replace=False)


def compute_point_or_global_qhat(
    calib_files: List[Path],
    alpha: float,
    eps: float,
    mode: str,
    score_sample_per_file: int | None = None,
    score_sample_seed: int = 42,
) -> Tuple[np.ndarray, Dict]:
    """Compute qhat for global_abs or point_sigma.

  Without subsampling, scores are accumulated channel-wise to limit memory.
  With --score-sample-per-file, each calibration file contributes at most N
  randomly sampled points (same indices across channels); pooled scores
  from all files are used for the conformal quantile.
    """
    assert mode in ("global_abs", "point_sigma")

    qhat = np.zeros(4, dtype=np.float64)
    n_points_per_channel = np.zeros(4, dtype=np.int64)
    n_raw_points_total = 0

    if score_sample_per_file is not None:
        chunks_per_ch: List[List[np.ndarray]] = [[] for _ in range(4)]

        for file_idx, f in enumerate(calib_files):
            pred, target, sigma = load_pred_target_sigma(f, eps)
            n_raw_points_total += pred.shape[0]
            idx = sample_point_indices(
                pred.shape[0], score_sample_per_file, file_idx, score_sample_seed
            )
            pred_s = pred[idx]
            target_s = target[idx]
            sigma_s = sigma[idx]

            if mode == "global_abs":
                scores = np.abs(target_s - pred_s)
            else:
                scores = np.abs(target_s - pred_s) / sigma_s

            scores = scores.astype(np.float32, copy=False)
            for ch in range(4):
                chunks_per_ch[ch].append(scores[:, ch])
                n_points_per_channel[ch] += scores.shape[0]

            del pred, target, sigma, pred_s, target_s, sigma_s, scores

        for ch in range(4):
            scores_ch = np.concatenate(chunks_per_ch[ch], axis=0)
            qhat[ch] = finite_sample_quantile_1d(scores_ch, alpha)
        del chunks_per_ch
    else:
        for ch in range(4):
            chunks = []
            total = 0

            for f in calib_files:
                pred, target, sigma = load_pred_target_sigma(f, eps)
                n_raw_points_total += pred.shape[0]
                if mode == "global_abs":
                    score = np.abs(target[:, ch] - pred[:, ch])
                else:
                    score = np.abs(target[:, ch] - pred[:, ch]) / sigma[:, ch]

                score = score.astype(np.float32, copy=False)
                chunks.append(score)
                total += score.shape[0]

                del pred, target, sigma, score

            scores_ch = np.concatenate(chunks, axis=0)
            qhat[ch] = finite_sample_quantile_1d(scores_ch, alpha)
            n_points_per_channel[ch] = total
            del chunks, scores_ch

    meta: Dict = {
        "n_calibration_points_per_channel": {
            CHANNELS[i]: int(n_points_per_channel[i]) for i in range(4)
        },
        "n_calibration_points_raw_total": int(n_raw_points_total),
    }
    if score_sample_per_file is not None:
        meta["score_sample_per_file"] = int(score_sample_per_file)
        meta["score_sample_seed"] = int(score_sample_seed)
        meta["score_sampling"] = (
            f"random {score_sample_per_file} points per calibration file, "
            "pooled across files for qhat"
        )
    return qhat, meta


def reduce_case_score(scores_case: np.ndarray, alpha: float, case_score: str) -> np.ndarray:
    """Reduce point scores in one case to one score per channel."""
    if case_score == "max":
        return np.nanmax(scores_case, axis=0).astype(np.float64)
    if case_score == "mean":
        return np.nanmean(scores_case, axis=0).astype(np.float64)
    if case_score == "quantile":
        return finite_sample_quantile_2d_channelwise(scores_case, alpha)
    raise ValueError(f"Unknown case_score={case_score}")


def compute_case_qhat(
    calib_files: List[Path],
    alpha: float,
    eps: float,
    case_score: str,
) -> Tuple[np.ndarray, Dict]:
    """Compute case-based normalized qhat."""
    case_scores = []

    for f in calib_files:
        pred, target, sigma = load_pred_target_sigma(f, eps)
        scores = np.abs(target - pred) / sigma
        score_case = reduce_case_score(scores, alpha, case_score)
        case_scores.append(score_case)
        del pred, target, sigma, scores

    case_scores_arr = np.stack(case_scores, axis=0)  # (n_cases, 4)
    qhat = finite_sample_quantile_2d_channelwise(case_scores_arr, alpha)

    meta = {
        "case_score_reduction": case_score,
        "n_calibration_cases": int(case_scores_arr.shape[0]),
        "case_scores_mean": {
            CHANNELS[i]: float(np.mean(case_scores_arr[:, i])) for i in range(4)
        },
        "case_scores_max": {
            CHANNELS[i]: float(np.max(case_scores_arr[:, i])) for i in range(4)
        },
    }
    return qhat, meta


def evaluate_one_mode_on_file(
    npz_path: Path,
    qhat: np.ndarray,
    eps: float,
    mode: str,
) -> Dict:
    pred, target, sigma = load_pred_target_sigma(npz_path, eps)

    if mode == "global_abs":
        half_width = np.broadcast_to(qhat.reshape(1, 4), pred.shape)
    elif mode in ("point_sigma", "case_sigma"):
        half_width = qhat.reshape(1, 4) * sigma
    else:
        raise ValueError(f"Unknown mode={mode}")

    lower = pred - half_width
    upper = pred + half_width
    covered = (target >= lower) & (target <= upper)
    width = 2.0 * half_width

    out = {
        "file": npz_path.name,
        "n_points": int(pred.shape[0]),
        "coverage": {CHANNELS[i]: float(np.mean(covered[:, i])) for i in range(4)},
        "mean_width": {CHANNELS[i]: float(np.mean(width[:, i])) for i in range(4)},
        "median_width": {CHANNELS[i]: float(np.median(width[:, i])) for i in range(4)},
    }

    del pred, target, sigma, half_width, lower, upper, covered, width
    return out


def evaluate_modes(
    test_files: List[Path],
    qhats: Dict[str, np.ndarray],
    eps: float,
) -> Tuple[Dict, List[Dict]]:
    """Evaluate all modes on test files.

    Produces both pooled point coverage and mean case coverage.
    """
    # Accumulators
    point_total = {m: np.zeros(4, dtype=np.int64) for m in MODES}
    point_covered = {m: np.zeros(4, dtype=np.int64) for m in MODES}
    width_sum = {m: np.zeros(4, dtype=np.float64) for m in MODES}
    case_coverages = {m: [] for m in MODES}
    case_widths = {m: [] for m in MODES}
    per_case_rows = []

    for f in test_files:
        pred, target, sigma = load_pred_target_sigma(f, eps)
        n = pred.shape[0]

        for mode in MODES:
            qhat = qhats[mode]
            if mode == "global_abs":
                half_width = np.broadcast_to(qhat.reshape(1, 4), pred.shape)
            else:
                half_width = qhat.reshape(1, 4) * sigma

            lower = pred - half_width
            upper = pred + half_width
            covered = (target >= lower) & (target <= upper)
            width = 2.0 * half_width

            cov_case = np.mean(covered, axis=0)
            width_case = np.mean(width, axis=0)

            point_total[mode] += n
            point_covered[mode] += covered.sum(axis=0).astype(np.int64)
            width_sum[mode] += width.sum(axis=0)

            case_coverages[mode].append(cov_case)
            case_widths[mode].append(width_case)

            row = {
                "mode": mode,
                "file": f.name,
                "n_points": int(n),
            }
            for i, ch in enumerate(CHANNELS):
                row[f"coverage_{ch}"] = float(cov_case[i])
                row[f"mean_width_{ch}"] = float(width_case[i])
            per_case_rows.append(row)

            del half_width, lower, upper, covered, width

        del pred, target, sigma

    summary = {}

    for mode in MODES:
        case_cov = np.stack(case_coverages[mode], axis=0)
        case_wid = np.stack(case_widths[mode], axis=0)

        pooled_cov = point_covered[mode] / point_total[mode]
        pooled_width = width_sum[mode] / point_total[mode]

        summary[mode] = {
            "point_pooled_coverage": {
                CHANNELS[i]: float(pooled_cov[i]) for i in range(4)
            },
            "point_pooled_mean_width": {
                CHANNELS[i]: float(pooled_width[i]) for i in range(4)
            },
            "mean_case_coverage": {
                CHANNELS[i]: float(np.mean(case_cov[:, i])) for i in range(4)
            },
            "std_case_coverage": {
                CHANNELS[i]: float(np.std(case_cov[:, i], ddof=0)) for i in range(4)
            },
            "mean_case_width": {
                CHANNELS[i]: float(np.mean(case_wid[:, i])) for i in range(4)
            },
            "std_case_width": {
                CHANNELS[i]: float(np.std(case_wid[:, i], ddof=0)) for i in range(4)
            },
        }

    return summary, per_case_rows


def write_csv(path: Path, rows: List[Dict], fieldnames: List[str]) -> None:
    with open(path, "w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=fieldnames)
        w.writeheader()
        for r in rows:
            w.writerow(r)


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--calib-dir", type=Path, default=None)
    ap.add_argument(
        "--calib-glob",
        type=str,
        default=None,
        help=(
            "Glob for calibration .npz files, e.g. "
            "'runs/cvplus/fold_*/cvplus_oof/fold_*/*.npz' or "
            "'runs/cvplus/fold_*/cvplus_oof/fold_*' (directories)."
        ),
    )
    ap.add_argument("--test-dir", type=Path, required=True)
    ap.add_argument("--alpha", type=float, default=0.1)
    ap.add_argument("--eps", type=float, default=1e-6)
    ap.add_argument("--case-score", choices=["quantile", "max", "mean"], default="quantile",
                    help="How to reduce point scores to one score per case for case_sigma.")
    ap.add_argument(
        "--score-sample-per-file",
        type=int,
        default=None,
        help=(
            "For global_abs and point_sigma only: randomly sample this many points "
            "per calibration .npz before pooling scores for qhat. case_sigma is unchanged."
        ),
    )
    ap.add_argument(
        "--score-sample-seed",
        type=int,
        default=42,
        help="RNG seed for --score-sample-per-file (per-file indices are seed+file_idx).",
    )
    ap.add_argument("--out", type=Path, default=Path("results/cp_compare_global_point_case"))
    args = ap.parse_args()

    args.out.mkdir(parents=True, exist_ok=True)

    if args.calib_glob:
        calib_files = list_npz_from_glob(args.calib_glob)
    elif args.calib_dir:
        calib_files = list_npz(args.calib_dir)
    else:
        raise ValueError("Either --calib-dir or --calib-glob must be provided.")

    test_files = list_npz(args.test_dir)

    print(f"[INFO] calib files: {len(calib_files)}")
    print(f"[INFO] test files : {len(test_files)}")
    print(f"[INFO] alpha      : {args.alpha}")
    print(f"[INFO] case_score : {args.case_score}")
    if args.score_sample_per_file is not None:
        n_expected = args.score_sample_per_file * len(calib_files)
        print(
            f"[INFO] score_sample: {args.score_sample_per_file} points/file "
            f"x {len(calib_files)} files ~= {n_expected} pooled scores/mode "
            f"(seed={args.score_sample_seed})"
        )

    qhats: Dict[str, np.ndarray] = {}
    qhat_meta: Dict[str, Dict] = {}

    print("[INFO] Computing global_abs qhat...")
    qhats["global_abs"], qhat_meta["global_abs"] = compute_point_or_global_qhat(
        calib_files,
        args.alpha,
        args.eps,
        mode="global_abs",
        score_sample_per_file=args.score_sample_per_file,
        score_sample_seed=args.score_sample_seed,
    )

    print("[INFO] Computing point_sigma qhat...")
    qhats["point_sigma"], qhat_meta["point_sigma"] = compute_point_or_global_qhat(
        calib_files,
        args.alpha,
        args.eps,
        mode="point_sigma",
        score_sample_per_file=args.score_sample_per_file,
        score_sample_seed=args.score_sample_seed,
    )

    print("[INFO] Computing case_sigma qhat...")
    qhats["case_sigma"], qhat_meta["case_sigma"] = compute_case_qhat(
        calib_files, args.alpha, args.eps, args.case_score
    )

    qhat_json = {
        mode: {CHANNELS[i]: float(qhats[mode][i]) for i in range(4)}
        for mode in MODES
    }

    with open(args.out / "qhat.json", "w", encoding="utf-8") as f:
        json.dump({
            "alpha": args.alpha,
            "eps": args.eps,
            "case_score": args.case_score,
            "score_sample_per_file": args.score_sample_per_file,
            "score_sample_seed": args.score_sample_seed,
            "calib_dir": str(args.calib_dir) if args.calib_dir else None,
            "calib_glob": args.calib_glob,
            "qhat": qhat_json,
            "meta": qhat_meta,
        }, f, indent=2, ensure_ascii=False)

    print("[INFO] Evaluating modes on test set...")
    metrics, per_case_rows = evaluate_modes(test_files, qhats, args.eps)

    summary = {
        "alpha": args.alpha,
        "target_coverage": 1.0 - args.alpha,
        "eps": args.eps,
        "case_score": args.case_score,
        "score_sample_per_file": args.score_sample_per_file,
        "score_sample_seed": args.score_sample_seed,
        "calib_dir": str(args.calib_dir) if args.calib_dir else None,
        "calib_glob": args.calib_glob,
        "n_calibration_cases": len(calib_files),
        "n_test_cases": len(test_files),
        "qhat": qhat_json,
        "metrics": metrics,
        "definitions": {
            "global_abs": "score=abs(target-pred); interval=pred +/- q_abs",
            "point_sigma": "score=abs(target-pred)/sigma; interval=pred +/- q_sigma*sigma",
            "case_sigma": "case-level normalized score, then conformal quantile across cases; interval=pred +/- q_case*sigma",
        },
    }

    with open(args.out / "summary.json", "w", encoding="utf-8") as f:
        json.dump(summary, f, indent=2, ensure_ascii=False)

    # Compact table: one row per mode/channel.
    table_rows = []
    for mode in MODES:
        for ch in CHANNELS:
            table_rows.append({
                "mode": mode,
                "channel": ch,
                "qhat": qhat_json[mode][ch],
                "point_pooled_coverage": metrics[mode]["point_pooled_coverage"][ch],
                "point_pooled_mean_width": metrics[mode]["point_pooled_mean_width"][ch],
                "mean_case_coverage": metrics[mode]["mean_case_coverage"][ch],
                "mean_case_width": metrics[mode]["mean_case_width"][ch],
                "std_case_coverage": metrics[mode]["std_case_coverage"][ch],
                "std_case_width": metrics[mode]["std_case_width"][ch],
            })

    write_csv(
        args.out / "comparison_table.csv",
        table_rows,
        [
            "mode", "channel", "qhat",
            "point_pooled_coverage", "point_pooled_mean_width",
            "mean_case_coverage", "mean_case_width",
            "std_case_coverage", "std_case_width",
        ],
    )

    per_case_fields = ["mode", "file", "n_points"]
    for ch in CHANNELS:
        per_case_fields += [f"coverage_{ch}", f"mean_width_{ch}"]
    write_csv(args.out / "per_case_metrics.csv", per_case_rows, per_case_fields)

    print(json.dumps(summary, indent=2, ensure_ascii=False))
    print(f"[OK] Wrote:")
    print(f"  {args.out / 'summary.json'}")
    print(f"  {args.out / 'comparison_table.csv'}")
    print(f"  {args.out / 'per_case_metrics.csv'}")
    print(f"  {args.out / 'qhat.json'}")


if __name__ == "__main__":
    main()
